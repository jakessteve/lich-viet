// ── Helper: pick random variant ───────────────────────────────

/**
 * Pick a variant from an array, cycling through without repeat.
 * Uses a closure-based counter to ensure sequential non-repeating selection.
 */
export function createVariantPicker(variants: string[]): () => string {
    let index = 0;
    return () => {
        const result = variants[index % variants.length];
        index++;
        return result;
    };
}

/**
 * Replace %ARCHETYPE% and %ELEMENT% placeholders in a string.
 */
export function fillPlaceholders(
    text: string,
    archetype: string,
    element: string,
): string {
    return text
        .replace(/%ARCHETYPE%/g, archetype)
        .replace(/%ELEMENT%/g, element);
}
