// ── Orchestrator: interpretDivination Tests ───────────────────

describe('Orchestrator: interpretDivination', () => {
    test('produces valid DivineReadingSummary shape', () => {
        // Use number-based divination for deterministic input
        const divResult = performNumberBasedDivination({ num1: 5, num2: 3 });
        const summary = interpretDivination(divResult, 2);

        // Shape checks
        expect(summary).toHaveProperty('overallVerdict');
        expect(summary).toHaveProperty('theDungAssessment');
        expect(summary).toHaveProperty('temporalInfluence');
        expect(summary).toHaveProperty('hexagramMeaning');
        expect(summary).toHaveProperty('detailedExplanation');
        expect(summary).toHaveProperty('elementBreakdown');

        // Verdict is a valid value
        expect(['Cát', 'Hung', 'Bình']).toContain(summary.overallVerdict);

        // TheDungAssessment has all fields
        expect(summary.theDungAssessment).toHaveProperty('relationship');
        expect(summary.theDungAssessment).toHaveProperty('relationType');
        expect(summary.theDungAssessment).toHaveProperty('meaning');
        expect(summary.theDungAssessment).toHaveProperty('verdict');

        // Temporal influence has all fields
        expect(summary.temporalInfluence).toHaveProperty('strength');
        expect(summary.temporalInfluence).toHaveProperty('description');

        // Hexagram meaning has all fields
        expect(summary.hexagramMeaning).toHaveProperty('name');
        expect(summary.hexagramMeaning).toHaveProperty('image');
        expect(summary.hexagramMeaning).toHaveProperty('meaning');

        // Detailed explanation is a non-empty string
        expect(typeof summary.detailedExplanation).toBe('string');
        expect(summary.detailedExplanation.length).toBeGreaterThan(0);

        // Element breakdown
        expect(summary.elementBreakdown).toHaveProperty('theElement');
        expect(summary.elementBreakdown).toHaveProperty('dungElement');
        expect(summary.elementBreakdown).toHaveProperty('relation');
    });

    test('time-based divination also produces valid summary', () => {
        const divResult = performTimeBasedDivination({
            yearChiIndex: 5,
            lunarMonth: 2,
            lunarDay: 5,
            hourChiIndex: 3,
        });
        const summary = interpretDivination(divResult, 2);
        expect(['Cát', 'Hung', 'Bình']).toContain(summary.overallVerdict);
        expect(typeof summary.detailedExplanation).toBe('string');
    });

    test('throws for invalid lunar month', () => {
        const divResult = performNumberBasedDivination({ num1: 1, num2: 1 });
        expect(() => interpretDivination(divResult, 0)).toThrow(RangeError);
        expect(() => interpretDivination(divResult, 13)).toThrow(RangeError);
    });

    test('Cát verdict when assessment is Cát and strength is Vượng', () => {
        // Mộc (Thể) in Xuân (month 1) = Vượng
        // We need Dụng to be Bị Sinh for Thể = Cát
        // If Thể = Mộc and Dụng = Thủy → Thủy sinh Mộc → Bị Sinh → Cát
        // Let's manually construct a DivinationResult
        const divResult = createMockDivinationResult('Mộc', 'Thủy');
        const summary = interpretDivination(divResult, 1);
        expect(summary.theDungAssessment.verdict).toBe('Cát');
        expect(summary.temporalInfluence.strength).toBe('Vượng');
        expect(summary.overallVerdict).toBe('Cát');
    });

    test('Hung verdict when assessment is Hung and strength is weak', () => {
        // Thổ (Thể) in Xuân (month 1) = Tử
        // Dụng = Mộc → Mộc khắc Thổ → Bị Khắc → Hung 
        const divResult = createMockDivinationResult('Thổ', 'Mộc');
        const summary = interpretDivination(divResult, 1);
        expect(summary.theDungAssessment.verdict).toBe('Hung');
        expect(summary.temporalInfluence.strength).toBe('Tử');
        expect(summary.overallVerdict).toBe('Hung');
    });

    test('Bình verdict when assessment is Hung but strength is strong', () => {
        // Kim (Thể) in Thu (month 7) = Vượng
        // Dụng = Thủy → Kim Sinh Thủy → Sinh → Hung
        const divResult = createMockDivinationResult('Kim', 'Thủy');
        const summary = interpretDivination(divResult, 7);
        expect(summary.theDungAssessment.verdict).toBe('Hung');
        expect(summary.temporalInfluence.strength).toBe('Vượng');
        expect(summary.overallVerdict).toBe('Bình');
    });

    test('Bình verdict when assessment is Cát but strength is weak', () => {
        // Kim (Thể) in Hạ (month 4) = Tử
        // Dụng = Thổ → Thổ Sinh Kim → Bị Sinh → Cát
        const divResult = createMockDivinationResult('Kim', 'Thổ');
        const summary = interpretDivination(divResult, 4);
        expect(summary.theDungAssessment.verdict).toBe('Cát');
        expect(summary.temporalInfluence.strength).toBe('Tử');
        expect(summary.overallVerdict).toBe('Bình');
    });

    test('detailed explanation includes hexagram name', () => {
        const divResult = performNumberBasedDivination({ num1: 3, num2: 7 });
        const summary = interpretDivination(divResult, 6);
        expect(summary.detailedExplanation).toContain(divResult.mainHexagram.name);
    });

    test('element breakdown matches divination elements', () => {
        const divResult = performNumberBasedDivination({ num1: 10, num2: 4 });
        const summary = interpretDivination(divResult, 9);
        expect(summary.elementBreakdown.theElement).toBe(divResult.elements.theElement);
        expect(summary.elementBreakdown.dungElement).toBe(divResult.elements.dungElement);
    });
});

// ── Integration Risk 2: Leap Month in Interpreter ─────────────

describe('Integration Risk 2: Leap Month in Interpreter', () => {
    test('leap month produces same seasonal strength as non-leap (same month number)', () => {
        // The engine treats leap month 4 identically to regular month 4.
        // Both map to the same season (Hạ) and produce the same strength.
        const divResult = performNumberBasedDivination({ num1: 5, num2: 3 });
        const summaryNormal = interpretDivination(divResult, 4);   // Regular month 4
        const summaryLeap = interpretDivination(divResult, 4);   // Leap month 4 (same value)

        expect(summaryNormal.temporalInfluence.strength).toBe(summaryLeap.temporalInfluence.strength);
        expect(summaryNormal.overallVerdict).toBe(summaryLeap.overallVerdict);
    });

    test('all 12 month values produce valid interpretations (no leap-only crash)', () => {
        const divResult = performNumberBasedDivination({ num1: 7, num2: 2 });
        for (let month = 1; month <= 12; month++) {
            const summary = interpretDivination(divResult, month);
            expect(['Cát', 'Hung', 'Bình']).toContain(summary.overallVerdict);
            expect(summary.temporalInfluence.description).toBeTruthy();
        }
    });
});

