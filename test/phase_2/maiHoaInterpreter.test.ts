describe('US_MH_06: getElementInteraction', () => {
    // ── Self-relations (Tỷ Hòa) ──────────────────────────────
    const allElements: NguHanh[] = ['Kim', 'Thủy', 'Mộc', 'Hỏa', 'Thổ'];

    test.each(allElements)('%s vs itself → Tỷ Hòa', (element) => {
        expect(getElementInteraction(element, element)).toBe('Tỷ Hòa');
    });

    // ── Sinh (generates) chain: Kim→Thủy→Mộc→Hỏa→Thổ→Kim ──
    const sinhPairs: [NguHanh, NguHanh][] = [
        ['Kim', 'Thủy'],
        ['Thủy', 'Mộc'],
        ['Mộc', 'Hỏa'],
        ['Hỏa', 'Thổ'],
        ['Thổ', 'Kim'],
    ];

    test.each(sinhPairs)('%s sinh %s → Sinh', (source, target) => {
        expect(getElementInteraction(source, target)).toBe('Sinh');
    });

    // ── Bị Sinh (is generated by) — inverse of Sinh ──────────
    const biSinhPairs: [NguHanh, NguHanh][] = [
        ['Thủy', 'Kim'],
        ['Mộc', 'Thủy'],
        ['Hỏa', 'Mộc'],
        ['Thổ', 'Hỏa'],
        ['Kim', 'Thổ'],
    ];

    test.each(biSinhPairs)('%s bị sinh bởi %s → Bị Sinh', (source, target) => {
        expect(getElementInteraction(source, target)).toBe('Bị Sinh');
    });

    // ── Khắc (controls) chain: Kim→Mộc→Thổ→Thủy→Hỏa→Kim ───
    const khacPairs: [NguHanh, NguHanh][] = [
        ['Kim', 'Mộc'],
        ['Mộc', 'Thổ'],
        ['Thổ', 'Thủy'],
        ['Thủy', 'Hỏa'],
        ['Hỏa', 'Kim'],
    ];

    test.each(khacPairs)('%s khắc %s → Khắc', (source, target) => {
        expect(getElementInteraction(source, target)).toBe('Khắc');
    });

    // ── Bị Khắc (is controlled by) — inverse of Khắc ────────
    const biKhacPairs: [NguHanh, NguHanh][] = [
        ['Mộc', 'Kim'],
        ['Thổ', 'Mộc'],
        ['Thủy', 'Thổ'],
        ['Hỏa', 'Thủy'],
        ['Kim', 'Hỏa'],
    ];

    test.each(biKhacPairs)('%s bị khắc bởi %s → Bị Khắc', (source, target) => {
        expect(getElementInteraction(source, target)).toBe('Bị Khắc');
    });

    // ── Total: 5×5 = 25 pairs are fully covered ──────────────
    test('complete 5×5 matrix coverage', () => {
        let count = 0;
        for (const a of allElements) {
            for (const b of allElements) {
                const result = getElementInteraction(a, b);
                expect(['Sinh', 'Khắc', 'Bị Sinh', 'Bị Khắc', 'Tỷ Hòa']).toContain(result);
                count++;
            }
        }
        expect(count).toBe(25);
    });
});

describe('US_MH_06: assessTheDung', () => {
    test('Dụng Sinh Thể (Cát)', () => {
        // Thủy (Thể) vs Kim (Dụng): Kim Sinh Thủy → Thể is generated = Bị Sinh
        const result = assessTheDung('Thủy', 'Kim');
        expect(result.relationship).toBe('Dụng Sinh Thể');
        expect(result.relationType).toBe('Bị Sinh');
        expect(result.verdict).toBe('Cát');
        expect(result.meaning).toContain('Được lợi');
    });

    test('Thể Sinh Dụng (Hung)', () => {
        // Kim (Thể) vs Thủy (Dụng): Kim Sinh Thủy → Thể expends energy = Sinh
        const result = assessTheDung('Kim', 'Thủy');
        expect(result.relationship).toBe('Thể Sinh Dụng');
        expect(result.relationType).toBe('Sinh');
        expect(result.verdict).toBe('Hung');
        expect(result.meaning).toContain('hao tổn');
    });

    test('Thể Khắc Dụng (Bình)', () => {
        // Kim (Thể) vs Mộc (Dụng): Kim Khắc Mộc → Thể controls
        const result = assessTheDung('Kim', 'Mộc');
        expect(result.relationship).toBe('Thể Khắc Dụng');
        expect(result.relationType).toBe('Khắc');
        expect(result.verdict).toBe('Bình');
        expect(result.meaning).toContain('khó nhọc');
    });

    test('Dụng Khắc Thể (Hung)', () => {
        // Mộc (Thể) vs Kim (Dụng): Mộc Bị Khắc bởi Kim → Dụng controls Thể
        const result = assessTheDung('Mộc', 'Kim');
        expect(result.relationship).toBe('Dụng Khắc Thể');
        expect(result.relationType).toBe('Bị Khắc');
        expect(result.verdict).toBe('Hung');
        expect(result.meaning).toContain('Đại hung');
    });

    test('Tỷ Hòa (Cát)', () => {
        const result = assessTheDung('Hỏa', 'Hỏa');
        expect(result.relationship).toBe('Thể Dụng Tỷ Hòa');
        expect(result.relationType).toBe('Tỷ Hòa');
        expect(result.verdict).toBe('Cát');
        expect(result.meaning).toContain('Bình hòa');
    });
});

