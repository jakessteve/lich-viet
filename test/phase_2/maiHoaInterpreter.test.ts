/**
 * Creates a mock DivinationResult with specific Thể/Dụng elements.
 * Uses Trigram IDs to match the desired elements.
 */
function createMockDivinationResult(
    theElement: NguHanh,
    dungElement: NguHanh,
): DivinationResult {
    // Find trigrams that match the desired elements
    const theTrigramId = findTrigramIdByElement(theElement);
    const dungTrigramId = findTrigramIdByElement(dungElement);

    // Moving line 1 → lower is Dụng, upper is Thể
    // So upper = Thể element, lower = Dụng element
    const mainHexagram: Hexagram = {
        id: 1,
        name: 'Test Hexagram',
        upper: theTrigramId,
        lower: dungTrigramId,
        meaning: 'Test meaning',
        image: 'Test image',
    };

    return {
        mainHexagram,
        mutualHexagram: mainHexagram,
        changedHexagram: mainHexagram,
        movingLine: 1,
        theTrigram: 'upper',
        dungTrigram: 'lower',
        elements: {
            theElement,
            dungElement,
        },
    };
}

/**
 * Finds the first trigram ID that has the specified Ngũ Hành element.
 */
function findTrigramIdByElement(element: NguHanh): number {
    const elementToTrigramId: Record<NguHanh, number> = {
        'Kim': 1,   // Càn = Kim
        'Hỏa': 3,   // Ly = Hỏa
        'Mộc': 4,   // Chấn = Mộc (actually Tốn is also Mộc)
        'Thủy': 6,  // Khảm = Thủy
        'Thổ': 8,   // Khôn = Thổ
    };
    return elementToTrigramId[element];
}
